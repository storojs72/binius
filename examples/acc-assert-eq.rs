fn assert_eq_gadget(
	builder: &mut ConstraintSystemBuilder<OptimalUnderlier, BinaryField128b>,
	input: &[bool],
) {
	assert!(input.len() > 0);

	fn set_bit(a: u128, index: usize) -> u128 {
		assert!(index < 128);
		let out = a | MASK[index];
		out
	}

	let zero_id = variable_u128::<_, _, BinaryField1b>(builder, "zero", LOG_SIZE, 0u128).unwrap();

	// Split input into 128-bit chunks, instantiate variable by setting its bits from the input and finally constraint it to be zero
	input
		.chunks(128)
		.into_iter()
		.enumerate()
		.for_each(|(chunk_index, chunk)| {
			let mut value = 0u128;
			for (bit_index, one) in chunk.iter().enumerate() {
				if *one {
					value = set_bit(value, bit_index);
				}
			}
			let value_id = variable_u128::<_, _, BinaryField1b>(
				builder,
				format!("packed_value::{}", chunk_index),
				LOG_SIZE,
				value,
			)
			.unwrap();
			builder.assert_zero(
				[value_id, zero_id],
				arith_expr!([val, zero] = val - zero).convert_field(),
			);
		});
}

fn main() {
	fn positive_test(input: &[bool]) {
		let allocator = bumpalo::Bump::new();
		let mut builder =
			ConstraintSystemBuilder::<OptimalUnderlier, BinaryField128b>::new_with_witness(
				&allocator,
			);

		assert_eq_gadget(&mut builder, input);

		let witness = builder.take_witness().unwrap();
		let cs = builder.build().unwrap();

		let (prove_no_issues, verify_no_issues) = prove_verify_test(witness, cs);
		assert!(prove_no_issues);
		assert!(verify_no_issues);
	}

	fn negative_test(input: &[bool]) {
		let allocator = bumpalo::Bump::new();
		let mut builder =
			ConstraintSystemBuilder::<OptimalUnderlier, BinaryField128b>::new_with_witness(
				&allocator,
			);

		assert_eq_gadget(&mut builder, input);

		let witness = builder.take_witness().unwrap();
		let cs = builder.build().unwrap();

		let (prove_no_issues, verify_no_issues) = prove_verify_test(witness, cs);
		assert!(prove_no_issues);
		assert!(!verify_no_issues); // issue on verification is expected
	}

	let input = [false; 1];
	positive_test(&input);

	let input = [false; 2];
	positive_test(&input);

	let input = [false; 10];
	positive_test(&input);

	let input = [false; 1000000];
	positive_test(&input);

	let mut input = [false; 1000000];
	input[9999] = true;
	negative_test(&input);

	let input = [true; 1];
	negative_test(&input);

	let input = [true; 2];
	negative_test(&input);

	let input = [true; 10];
	negative_test(&input);

	let input = [true; 1000000];
	negative_test(&input);

	let mut input = [false; 10];
	input[0] = true;
	input[2] = true;
	negative_test(&input);

	input[3] = true;
	negative_test(&input);
}

fn prove_verify_test(
	witness: MultilinearExtensionIndex<OptimalUnderlier, BinaryField128b>,
	constraints: ConstraintSystem<BinaryField128b>,
) -> (bool, bool) {
	let domain_factory = DefaultEvaluationDomainFactory::default();
	let backend = make_portable_backend();
	let proof = constraint_system::prove::<
		OptimalUnderlier,
		CanonicalTowerFamily,
		BinaryField8b,
		_,
		_,
		GroestlHasher<BinaryField128b>,
		GroestlDigestCompression<BinaryField8b>,
		HasherChallenger<Groestl256>,
		_,
	>(&constraints, 1usize, 100usize, witness, &domain_factory, &backend);

	let prove_no_issues = proof.is_ok();
	if !prove_no_issues {
		// Since we have issue on proving, verification is also an issue
		return (false, false);
	}

	let out = constraint_system::verify::<
		OptimalUnderlier,
		CanonicalTowerFamily,
		_,
		_,
		GroestlHasher<BinaryField128b>,
		GroestlDigestCompression<BinaryField8b>,
		HasherChallenger<Groestl256>,
	>(&constraints, 1usize, 100usize, &domain_factory, vec![], proof.unwrap());

	let verify_no_issues = out.is_ok();
	(prove_no_issues, verify_no_issues)
}
